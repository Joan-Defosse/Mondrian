package Struct;

import java.awt.Color;

public class Palette {

    // PUBLIC ATTRIBUTES ===================================== //

    public Color lineColor;
    public Color colorA;
    public Color colorB;
    public Color colorC;
    public Color colorD;
    public Color colorE;

    // PUBLIC CONSTRUCTORS ===================================== //

    public Palette(Color lineColor, Color colorA, Color colorB, Color colorC, Color colorD, Color colorE) {

        this.lineColor = lineColor;
        this.colorA = colorA;
        this.colorB = colorB;
        this.colorC = colorC;
        this.colorD = colorD;
        this.colorE = colorE;
    }
}
